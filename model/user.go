func (user *User) Delete() error {
	if user.Id == 0 {
		return errors.New("id 为空！")
	}
	// Cascade KYC soft-delete to keep the account-deletion lifecycle consistent:
	// user soft-deleted → user_kycs and user_kyc_images both soft-deleted.
	// Errors are non-fatal (audit data) — only log via the returned error from
	// the DB.Delete call below.
	if kyc, err := GetKYCByUserId(user.Id); err == nil {
		_ = SoftDeleteKYCImagesByKYCId(kyc.Id)
	}
	_ = DB.Where("user_id = ?", user.Id).Delete(&UserKYC{}).Error
	// Same soft-delete cascade for enterprise certification.
	if ent, err := GetEnterpriseByUserId(user.Id); err == nil {
		_ = SoftDeleteEnterpriseImagesByEnterpriseId(ent.Id)
	}
	_ = DB.Where("user_id = ?", user.Id).Delete(&UserEnterprise{}).Error
	// 企业主账户删除跟随处理：软删其全部子账户、硬删名下绑定记录（设计 §4.3 异常路径，逃生通道）。
	// 级联清理与主体软删包裹在单事务内：避免进程中途崩溃残留「绑定已删、主账户还在」的半状态。
	// 该用户若本身是子账户，一并硬删其作为 sub_user 的绑定，避免悬空绑定。
	if err := DB.Transaction(func(tx *gorm.DB) error {
		cascadeDeleteSubAccountsForParent(tx, user.Id)
		_ = tx.Where("sub_user_id = ?", user.Id).Delete(&SubAccountTokenBinding{}).Error
		return tx.Delete(user).Error
	}); err != nil {
		return err
	}

	// 清除缓存
	return invalidateUserCache(user.Id)
}

