// FillParentUsernames 为子账户行（parent_user_id>0）批量填充其所属企业主账户的用户名，
// 供管理员用户列表展示归属关系。一次 IN 查询解决，避免逐行查导致 N+1。瞬态字段不入库。
func FillParentUsernames(users []*User) {
	parentIds := make([]int, 0)
	seen := make(map[int]struct{})
	for _, u := range users {
		if u != nil && u.ParentUserId > 0 {
			if _, ok := seen[u.ParentUserId]; !ok {
				seen[u.ParentUserId] = struct{}{}
				parentIds = append(parentIds, u.ParentUserId)
			}
		}
	}
	if len(parentIds) == 0 {
		return
	}
	type row struct {
		Id       int
		Username string
	}
	var rows []row
	// Unscoped：企业主账户可能已软删（但子账户尚未级联清理的边缘态），仍展示其名便于追溯。
	if err := DB.Unscoped().Model(&User{}).Select("id, username").
		Where("id IN ?", parentIds).Scan(&rows).Error; err != nil {
		common.SysLog("failed to fill parent usernames: " + err.Error())
		return
	}
	nameMap := make(map[int]string, len(rows))
	for _, r := range rows {
		nameMap[r.Id] = r.Username
	}
	for _, u := range users {
		if u != nil && u.ParentUserId > 0 {
			u.ParentUsername = nameMap[u.ParentUserId]
		}
	}
}

func GetAllUsers(pageInfo *common.PageInfo, kycStatus int, enterpriseStatus int) (users []*User, total int64, err error) {
	// Start transaction
	tx := DB.Begin()
	if tx.Error != nil {
		return nil, 0, tx.Error
	}
	defer func() {
		if r := recover(); r != nil {
			tx.Rollback()
		}
	}()

	baseQuery := tx.Unscoped().Model(&User{})
	if kycStatus != 0 {
		baseQuery = baseQuery.Where("kyc_status = ?", kycStatus)
	}
	// enterpriseStatus uses -1 as the "no filter" sentinel so an explicit 0
	// (未认证) can be queried; values >= 0 are real filters.
	if enterpriseStatus >= 0 {
		baseQuery = baseQuery.Where("enterprise_status = ?", enterpriseStatus)
	}

	// Get total count within transaction
	err = baseQuery.Count(&total).Error
	if err != nil {
		tx.Rollback()
		return nil, 0, err
	}

	// Get paginated users within same transaction
	err = baseQuery.Order("id desc").Limit(pageInfo.GetPageSize()).Offset(pageInfo.GetStartIdx()).Omit("password").Find(&users).Error
	if err != nil {
		tx.Rollback()
		return nil, 0, err
	}

	// Commit transaction
	if err = tx.Commit().Error; err != nil {
		return nil, 0, err
	}

	return users, total, nil
}

func SearchUsers(keyword string, group string, startIdx int, num int, kycStatus int, enterpriseStatus int) ([]*User, int64, error) {
	var users []*User
	var total int64
	var err error

	// 开始事务
	tx := DB.Begin()
	if tx.Error != nil {
		return nil, 0, tx.Error
	}
	defer func() {
		if r := recover(); r != nil {
			tx.Rollback()
		}
	}()

	// 构建基础查询
	query := tx.Unscoped().Model(&User{})

	// 构建搜索条件
	likeCondition := "username LIKE ? OR email LIKE ? OR display_name LIKE ?"

	// 尝试将关键字转换为整数ID
	keywordInt, err := strconv.Atoi(keyword)
	if err == nil {
		// 如果是数字，同时搜索ID和其他字段
		likeCondition = "id = ? OR " + likeCondition
		if group != "" {
			query = query.Where("("+likeCondition+") AND "+commonGroupCol+" = ?",
				keywordInt, "%"+keyword+"%", "%"+keyword+"%", "%"+keyword+"%", group)
		} else {
			query = query.Where(likeCondition,
				keywordInt, "%"+keyword+"%", "%"+keyword+"%", "%"+keyword+"%")
		}
	} else {
		// 非数字关键字，只搜索字符串字段
		if group != "" {
			query = query.Where("("+likeCondition+") AND "+commonGroupCol+" = ?",
				"%"+keyword+"%", "%"+keyword+"%", "%"+keyword+"%", group)
		} else {
			query = query.Where(likeCondition,
				"%"+keyword+"%", "%"+keyword+"%", "%"+keyword+"%")
		}
	}

	if kycStatus != 0 {
		query = query.Where("kyc_status = ?", kycStatus)
	}
	// -1 = no filter; >= 0 (including 0 = 未认证) is a real filter.
	if enterpriseStatus >= 0 {
		query = query.Where("enterprise_status = ?", enterpriseStatus)
	}

	// 获取总数
	err = query.Count(&total).Error
	if err != nil {
		tx.Rollback()
		return nil, 0, err
	}

	// 获取分页数据
	err = query.Omit("password").Order("id desc").Limit(num).Offset(startIdx).Find(&users).Error
	if err != nil {
		tx.Rollback()
		return nil, 0, err
	}

	// 提交事务
	if err = tx.Commit().Error; err != nil {
		return nil, 0, err
	}

	return users, total, nil
}

